close all;
rng default;
A = randn([500,2]);
B = randn([500,2]);
A(:,1) = A(:,1) / 2;
A(:,1) = A(:,1) - 0.75;
B(:,1) = B(:,1) / 2;
B(:,1) = B(:,1) + 0.75;
figure;
hold on;
scatter([A(:,1);B(:,1)], [A(:,2);B(:,2)]);
ellipse(1.25, 1, 0, 0, 0, 'r');
hold off;
title('Figure 1');
saveas(gcf, 'img/1.svg');
figure;
hold on;
scatter(A(:,1), A(:,2));
scatter(B(:,1), B(:,2));
ellipse(0.75, 1, 0, -0.75, 0, 'r');
ellipse(0.75, 1, 0, 0.75, 0, 'b');
hold off;
title('Figure 2');
saveas(gcf, 'img/2.svg');
